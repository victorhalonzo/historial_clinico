    private void listar(){
        
        PacienteDAO pDAO= new PacienteDAO();
        
        try {
            ArrayList<Paciente> pacientes= pDAO.listarPacientes();
            
            DefaultTableModel modelo= new DefaultTableModel(){
                
                @Override
                public boolean isCellEditable(int row, int col){
                    return false;
                }
            };
            modelo.addColumn("NOMBRES");
            modelo.addColumn("APELLIDOS");
            modelo.addColumn("SEXO");
            modelo.addColumn("EDAD");
            modelo.addColumn("DOMICILIO");
            modelo.addColumn("LUGAR DE NACIMIENTO");
            modelo.addColumn("ESTADO CIVIL");
            
            for(Paciente paciente: pacientes){
                String[] fila= new String[7];
                fila[0]= paciente.getNombres();
                fila[1]=paciente.getApellidos();
                fila[2]=String.valueOf(paciente.getSexo());
                fila[3]=String.valueOf(paciente.getEdad());
                fila[4]=paciente.getDomicilio();
                fila[5]=paciente.getLugarNacimiento();
                fila[6]=paciente.getEstadoCivil();
                
                modelo.addRow(fila);
            }
            
            tblPacientes.setModel(modelo);
            
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }
    }
    
