    private void tblImagenesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblImagenesMouseClicked
        // TODO add your handling code here:
        int column=tblImagenes.getColumnModel().getColumnIndexAtX(evt.getX());
        int row= evt.getY() / tblImagenes.getRowHeight();
        if(row<tblImagenes.getRowCount() && row>=0 && column<tblImagenes.getColumnCount() && column>=0){
            int id=(int) tblImagenes.getValueAt(row, 0);
            String ext= String.valueOf(tblImagenes.getValueAt(row, 3));
            Object value=tblImagenes.getValueAt(row, column);
            if(value instanceof JButton){
                ((JButton) value).doClick();
                JButton boton=(JButton) value;
                if(boton.getText().equals("vacio")){
                    JOptionPane.showMessageDialog(null, "no hay archivo");
                }else{
                    ImagenDAO imgDAO= new ImagenDAO();
                    imgDAO.abrirImagen(id);
                    try{
                        Desktop.getDesktop().open(new File("new"+ext));
                    }catch(Exception e){
                        
                    }
                }
            }
        }
    }//GEN-LAST:event_tblImagenesMouseClicked


