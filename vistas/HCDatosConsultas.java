    public void datosReceta(int idConsulta) {
        HCDAO hc = new HCDAO();

        try {
            ArrayList<Receta> receta = hc.datosReceta(idConsulta);

            DefaultTableModel modelo = new DefaultTableModel() {

                @Override
                public boolean isCellEditable(int row, int col) {
                    return false;
                }
            };

            modelo.addColumn("Medicamento");
            modelo.addColumn("Cantidad");
            

            for (Receta r : receta) {
                String[] fila = new String[2];
                fila[0] = r.getNombreMedicamento();
                fila[1] = String.valueOf(r.getCantidad());
                
                modelo.addRow(fila);
            }

            tblReceta.setModel(modelo);

            
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }

    }
    
    public void mostrarImagenes(int idConsulta) {
        HCDAO hc = new HCDAO();

        try {
            ArrayList<Imagen> imagenes = hc.imagenesConsulta(idConsulta);

            tblImagenes.setDefaultRenderer(Object.class, new btTablaImagenes());
            DefaultTableModel modelo = new DefaultTableModel() {

                @Override
                public boolean isCellEditable(int row, int col) {
                    return false;
                }
            };

            modelo.addColumn("Id");
            modelo.addColumn("Nombre");
            modelo.addColumn("Archivo");
            modelo.addColumn("Extensión");

            for (Imagen img : imagenes) {
                Object[] fila = new Object[4];
                fila[0] = img.getId();
                fila[1] = img.getNombre();
                if (img.getArchivo() != null) {
                    fila[2] = new JButton("Ver imagen");
                } else {
                    fila[2] = new JButton("vacio");
                }
                fila[3]= img.getExtension();

                modelo.addRow(fila);
            }

            tblImagenes.setModel(modelo);

            tblImagenes.getColumnModel().getColumn(0).setMaxWidth(0);
            tblImagenes.getColumnModel().getColumn(0).setMinWidth(0);
            tblImagenes.getTableHeader().getColumnModel().getColumn(0).setMaxWidth(0);
            tblImagenes.getTableHeader().getColumnModel().getColumn(0).setMinWidth(0);
            tblImagenes.getColumnModel().getColumn(3).setMaxWidth(0);
            tblImagenes.getColumnModel().getColumn(3).setMinWidth(0);
            tblImagenes.getTableHeader().getColumnModel().getColumn(3).setMaxWidth(0);
            tblImagenes.getTableHeader().getColumnModel().getColumn(3).setMinWidth(0);

        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        }

    }
    
