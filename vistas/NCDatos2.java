    private void btAgregarImagenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAgregarImagenActionPerformed
        // TODO add your handling code here:
        DefaultTableModel modelo = (DefaultTableModel) tblImagenes.getModel();
        JFileChooser j = new JFileChooser();
        FileNameExtensionFilter fi = new FileNameExtensionFilter("imagenes y pdf", "pdf", "jpg", "jpeg", "png");
        j.setFileFilter(fi);
        int se = j.showOpenDialog(this);
        if (se == 0) {
            modelo.addRow(new Object[]{j.getSelectedFile().getName(), j.getSelectedFile().getAbsolutePath()});

        }
    }//GEN-LAST:event_btAgregarImagenActionPerformed

