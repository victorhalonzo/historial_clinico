    private void tblRecetaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblRecetaKeyTyped
        // TODO add your handling code here:

        if (tblReceta.getSelectedColumn() == 1) {
            int key = evt.getKeyChar();

            boolean numeros = key >= 48 && key <= 57;

            if (!numeros) {
                evt.consume();
            }
        }

    }//GEN-LAST:event_tblRecetaKeyTyped

